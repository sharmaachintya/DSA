#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main()
{
    int a = 4;
    int b = 6;

    cout<<"a & b -> "<<(a&b)<<endl;
    cout<<"a | b -> "<<(a|b)<<endl; 
    cout<<" ~a -> "<<~a<<endl; 
    cout<<"a ^ b -> "<<(a^b)<<endl; 

    cout<<(5<<1)<<endl;
    cout<<(3<<2)<<endl;
    cout<<(5>>2)<<endl;

}